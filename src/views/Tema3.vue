      p(data-aos="fade-down") El primer cuadrante (1) sería el de la parte superior derecha de la boca; el segundo (2), la superior izquierda; el tercer cuadrante (3) corresponde a la parte inferior izquierda; y el cuarto (4) a la inferior derecha, en dentición definitiva. Es decir, se numera en el sentido opuesto al de las agujas del reloj.

